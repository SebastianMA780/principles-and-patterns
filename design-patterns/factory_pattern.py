### --- Factory Pattern --- ###

# Factory Pattern is a creational design pattern that provides an interface for creating objects in a superclass,
# but allows subclasses to alter the type of objects that will be created.

# - Allow to create objects without specifying the exact class of object that will be created.
# - Encapsulate object creation logic in a separate class.

# When to use:
# - When a class can't anticipate the class of objects it must create.
# - The object creation requires complex logic.

# How to implement:
# - Create factory class that creates objects based on input.
